package com.demo.mmo.mmo_entity.game.entity.po;

public class FightInfo {
	/** 玩家id */
	private int roleId;
	/** x坐标 */
	private float x;
	/** y坐标 */
	private float y;

	public int getRoleId() {
		return roleId;
	}

	public void setRoleId(int roleId) {
		this.roleId = roleId;
	}

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}

}
